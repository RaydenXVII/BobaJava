    private void btnCalculatePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculatePurchaseActionPerformed
        // TODO add your handling code here:
        try {
        int milk = Integer.parseInt(tfBM.getText());
        int thaiTea = Integer.parseInt(tfBTT.getText());
        int redVelvet = Integer.parseInt(tfBRV.getText());
        int oreo = Integer.parseInt(tfOreo.getText());
        int jelly = Integer.parseInt(tfJelly.getText());
        
        // perhitungan total harga
        int totalPembelian = milk * hargaBobaMilk + thaiTea * hargaBobaThaiTea + redVelvet * hargaBobaRedVelvet + oreo * hargaOreo + jelly * hargaJelly;
        
        // tampilkan total harga
        tfTotalPrice.setText(String.valueOf(totalPembelian));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Inputan harus berupa angka", "Salah Input Karakter", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCalculatePurchaseActionPerformed

