    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        String milk = tfBM.getText();
        String thaiTea = tfBTT.getText();
        String rv = tfBRV.getText();
        String oreo = tfOreo.getText();
        String jelly = tfJelly.getText();
        String totalBeli = tfTotalPrice.getText();
        String totalBayar = tfTotalPayment.getText();
    
    try {
        int bayar = Integer.parseInt(tfTotalPayment.getText());
        int beli = Integer.parseInt(tfTotalPrice.getText());

        if (bayar >= beli) {
            int susuk = bayar - beli;
            // Lakukan sesuatu dengan nilai kembalian
            JOptionPane.showMessageDialog(null, "Total Bayar: " + bayar + "\nKembalian: " + susuk,
                    "Informasi Pembayaran",
                    JOptionPane.INFORMATION_MESSAGE);
 
                String[] isi = { milk, thaiTea, rv, oreo, jelly, totalBeli};
                model.addRow(isi);
                tfTotalPrice.setText("");
                tfTotalPayment.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Uang Anda Kurang");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error: Masukkan angka yang valid");
    }
    }//GEN-LAST:event_btnProcessActionPerformed

